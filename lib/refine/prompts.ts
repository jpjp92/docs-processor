export function buildMergeReviewPrompt(sections: string) {
  return `다음은 여러 이미지에서 각각 병렬로 추출한 Markdown 조각들이다. 조각들은 이미 올바른 문서 순서(쪽번호가 있으면 그 순서)대로 나열되어 있다.
이들을 하나의 논리적으로 일관된 실제 보고서 문서로 통합·정리한 뒤, 아래 형식 그대로 출력해라(코드펜스·다른 설명 금지):

@@NOTES
<유형> | <무엇을 왜 바꿨는지 한 줄>
<유형> | <...>
@@DOCUMENT
<최종 문서 Markdown>

규칙:
- 출력에는 반드시 @@NOTES 블록 다음 @@DOCUMENT 블록을 포함한다
- @@DOCUMENT 아래에는 @@NOTES, @@DOCUMENT 같은 제어 마커를 절대 다시 쓰지 않는다
- @@DOCUMENT는 반드시 하나의 # 최상위 제목으로 시작한다
- "이미지 1", "이미지 2", 파일명(.jpg/.png/.webp 등), 업로드 순번을 최종 문서의 제목/섹션 제목으로 쓰지 않는다
- 이미지 파일명은 필요하면 변경 사항 notes에만 언급하고, 본문 제목은 내용 기반 제목으로 바꾼다
- 최상위 제목 다음에는 반드시 "## 핵심 요약" 섹션을 만들고, 문서 전체에서 바로 읽어야 할 내용을 3~5개 불릿으로 정리한다
- 그 다음에는 표/차트의 주제를 나타내는 "## <내용 기반 섹션 제목>"을 만들고 표를 배치한다
- 문서 마지막에는 반드시 "## 종합 평가" 섹션을 만들고, 전체 표를 가로지르는 결론을 1개 짧은 문단 + 3개 불릿으로 정리한다
- 주어진 조각 순서를 문서의 기본 흐름으로 삼되, 문맥상 자연스럽게 이어지도록 연결
- 여러 조각에 걸쳐 잘린 문장·표·목록은 하나로 이어 붙일 것
- 반복되는 머리말·쪽번호·중복 내용은 정리(본문에 남은 "- 12 -" 같은 쪽번호 흔적 제거)
- OCR 오탈자 교정, 제목/목록/표 구조 일관성 확보
- 캡션, 표 제목, 설명 문단을 실제 문서처럼 배치하고, 흩어진 표는 논리적으로 통합한다
- 모델명, 그림 라벨, 주석처럼 표 안에 들어가야 할 정보는 가능한 한 표로 정리한다
- 단순 나열이 아니라 제목, 요약 문단, 섹션, 표, 불릿을 사용해 읽기 쉬운 보고서로 재구성한다
- 표나 차트가 있으면 표만 두지 말고, 각 표 바로 아래에 "해석" 또는 "읽는 법" 소제목을 붙여 2~4개 불릿으로 설명한다
- "핵심 요약", "읽는 법", "해석", "종합 평가"의 항목은 반드시 "- "로 시작하는 Markdown 불릿으로 작성한다
- 여러 표의 "읽는 법" 문장을 같은 표현으로 반복하지 말고, 벤치마크 정의/점수 방향 같은 일반 설명은 문서에서 처음 한 번만 쓴다
- 각 표 아래 설명은 표에서만 보이는 관찰 포인트(최고/최저, 격차, 조건별 변화, 모델군별 상대 위치)를 중심으로 쓴다
- 해석에는 비교 대상, 가장 높은 값/낮은 값, 눈에 띄는 격차, 난이도별 변화처럼 표에서 직접 확인되는 의미를 포함한다
- 수치 해석은 원문 표에 있는 값만 사용하고, 근거 없는 원인 추정이나 외부 지식은 추가하지 않는다
- "오픈 소스", "잠재력", "실제 적용", "개선 필요성"처럼 표에 직접 없는 성격 규정이나 미래 전망은 원문에 명시되어 있지 않으면 쓰지 않는다
- 이미지가 벤치마크/성능표라면 모델별 상대적 위치와 쉬운 과제 대비 어려운 과제에서의 성능 저하를 간결하게 설명한다
- 원문에 없는 새 사실은 절대 만들지 말 것
- @@NOTES의 <유형>은 통합/교정/구조/확인필요 중 하나. 바꾼 게 없으면 "확인 | 변경 없음" 한 줄
- @@DOCUMENT 본문은 평범한 Markdown 그대로(줄바꿈 자유)

권장 문서 구조:
# <내용 기반 문서 제목>

## 핵심 요약
- <핵심 1>
- <핵심 2>
- <핵심 3>

## <표/차트 주제>
<정리된 Markdown 표>

### 읽는 법
- <가장 중요한 비교>
- <최고/최저 또는 눈에 띄는 격차>
- <난이도·조건별 변화>

## 종합 평가
<전체 경향을 2~3문장으로 요약>
- <종합 결론 1>
- <종합 결론 2>
- <종합 결론 3>

조각들:
${sections}`;
}

export function buildPolishDocumentPrompt(document: string) {
  return `다음 Markdown은 이미지 OCR 결과를 통합한 초안이다. 초안의 사실과 수치는 유지하되, 실제 보고서처럼 구조와 설명을 보강해 최종 Markdown만 출력해라(코드펜스 금지).

반드시 지킬 것:
- 출력은 # 제목으로 시작한다
- "이미지 1", 파일명(.jpg/.png/.webp 등), 업로드 순번을 제목으로 쓰지 않는다
- 제목 바로 다음에 "## 핵심 요약"을 만들고 3~5개 불릿을 작성한다
- 표가 있으면 표 바로 아래에 "### 읽는 법" 또는 "### 해석"을 만들고 2~4개 불릿으로 설명한다
- 문서 마지막에 "## 종합 평가"를 만들고 1개 짧은 문단 + 3개 불릿을 작성한다
- "핵심 요약", "읽는 법", "해석", "종합 평가"의 항목은 반드시 "- "로 시작한다
- 여러 표의 설명 문장을 기계적으로 반복하지 말고 각 표의 최고/최저, 격차, 조건별 변화를 다르게 짚는다
- 벤치마크 정의나 "점수가 높을수록 좋다" 같은 일반 설명은 반복하지 말고 필요한 경우 첫 등장에만 짧게 쓴다
- 원문에 명시되지 않은 모델 성격, 미래 전망, 실제 적용 가능성은 쓰지 않는다
- 설명에는 표에 있는 값만 사용한다
- 최고/최저, 큰 격차, 조건별 변화처럼 표에서 직접 보이는 의미를 정리한다
- 원문에 없는 새 사실, 외부 지식, 추측은 추가하지 않는다
- @@NOTES, @@DOCUMENT 같은 제어 마커는 출력하지 않는다

초안:
${document}`;
}
